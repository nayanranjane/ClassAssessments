








int marksScored = Convert.ToInt32(Console.ReadLine());

if (marksScored > 90)
{
    Console.WriteLine("Distinction");
}
else if (marksScored > 75)
{
    Console.WriteLine("First Class ");
}
else if (marksScored > 35)
{
    Console.WriteLine("Pass");
}
else
{
    Console.WriteLine("Pass");
}